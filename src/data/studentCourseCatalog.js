function getYouTubeEmbedUrl(value) {
  const rawValue = String(value || "").trim();
  if (!rawValue) {
    return "";
  }

  const match = rawValue.match(
    /(?:youtu\.be\/|youtube(?:-nocookie)?\.com\/(?:watch\?v=|embed\/|shorts\/))([A-Za-z0-9_-]{11})/
  );

  if (!match) {
    return "";
  }

  return `https://www.youtube-nocookie.com/embed/${match[1]}?rel=0&modestbranding=1`;
}

function buildCourseHref(baseUrl, slug) {
  return `${baseUrl}/student/courses/${slug}`;
}

function buildCourseDetail(baseUrl, course) {
  const courseHref = buildCourseHref(baseUrl, course.slug);
  const sections = Array.isArray(course.sections)
    ? course.sections.map((section) => ({
        ...section,
        lessons: Array.isArray(section.lessons)
          ? section.lessons.map((lesson) => ({
              ...lesson,
              embedUrl: getYouTubeEmbedUrl(lesson.videoUrl),
              href: `${courseHref}/learn?lesson=${encodeURIComponent(lesson.id)}`
            }))
          : []
      }))
    : [];

  const flatLessons = sections.flatMap((section) => section.lessons || []);
  const firstLesson = flatLessons[0] || null;

  return {
    ...course,
    sections,
    lessonCount: flatLessons.length,
    firstLesson,
    href: courseHref,
    contentHref: `${courseHref}?tab=content`,
    commentsHref: `${courseHref}?tab=comments`,
    learnHref: firstLesson ? firstLesson.href : `${courseHref}/learn`
  };
}

function getStudentCourseCards(baseUrl = "") {
  return COURSE_CATALOG.map((course, index) => ({
    slug: course.slug,
    title: course.title,
    teacher: course.teacher,
    subtitle: course.subtitle,
    image: course.image,
    placeholder: `Ảnh học phần ${String(index + 1).padStart(2, "0")}`,
    href: buildCourseHref(baseUrl, course.slug),
    tone: course.tone
  }));
}

function getStudentCourseDetail(slug, baseUrl = "") {
  const course = COURSE_CATALOG.find((item) => item.slug === slug);
  return course ? buildCourseDetail(baseUrl, course) : null;
}

function getStudentCoursePlayer(slug, lessonId, baseUrl = "") {
  const detail = getStudentCourseDetail(slug, baseUrl);
  if (!detail) {
    return null;
  }

  const lessons = detail.sections.flatMap((section) => section.lessons || []);
  const requestedLessonId = String(lessonId || "").trim();
  const activeLesson =
    lessons.find((lesson) => {
      if (lesson.id === requestedLessonId) {
        return true;
      }

      return Array.isArray(lesson.legacyIds) && lesson.legacyIds.includes(requestedLessonId);
    }) ||
    detail.firstLesson ||
    null;

  return {
    ...detail,
    activeLesson
  };
}

